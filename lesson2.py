def get_choice():
    choice = input("Выбери 'правда' или 'действие': ").strip().lower()

    # Проверяем правильность ввода
    if choice == 'правда':
        return 'правда'
    elif choice == 'действие':
        return 'действие'
    else:
        return None  # Неправильный ввод


def main():
    print("Добро пожаловать в игру 'Правда или Действие'!")

    while True:
        choice = get_choice()

        # Если выбор был некорректным, предложим повторить попытку
        if choice is None:
            print("Некорректный выбор. Попробуй еще раз.")
        elif choice == 'правда':
            print(f"Правда: {random.choice(truths)}")
        elif choice == 'действие':
            print(f"Действие: {random.choice(dares)}")

        # Предлагаем сыграть еще раз
        play_again = input("Хочешь сыграть еще раз? (да/нет): ").strip().lower()

        # Проверка на продолжение игры
        if play_again == 'нет':
            print("Спасибо за игру!")
            break
        elif play_again != 'да':
            print("Некорректный выбор. Игра завершена.")
            break


if __name__ == "__main__":
    main()
